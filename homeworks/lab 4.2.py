
text = "Hello World"
uppercase = text.upper()
print("Uppercase:", uppercase)
lowercase = text.lower()
print("Lowercase:", lowercase)
title_case = text.title()
print("Title case:", title_case)
swapped = text.swapcase()
print("Swapped case:", swapped)
